package patterns;

import java.util.Scanner;

public class Pattern16 {
    // Program to print Symmetric-Void Pattern

    public static void printStars(int n) {

        for (int i = 1; i <= n; i++) {
            System.out.print("*");
        }
    }

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);


        scanner.close();
    }
}
