package patterns;

import java.util.Scanner;

public class Pattern18 {
    // Program to print Hollow Rectangle Pattern

    public static void printStars(int n) {

        for (int i = 1; i <= n; i++) {
            System.out.print("*");
        }
    }
    
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        scanner.close();
    }
}
